#include "libft.h"

int	ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb <= 1)
		return (0);
	while (i * i != nb && i * i < nb)
		i++;
	if (nb == i * i)
		return (i);
	else
		return (0);
}
